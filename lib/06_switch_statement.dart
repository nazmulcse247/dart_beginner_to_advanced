void main(){
  // dart switch statement

  int grade = 50;

  switch(grade){
    case 90:
      print("A+");
      break;
    case 80:
      print("A");
      break;
    case 70:
      print("B+");
      break;
    case 60:
      print("B");
      break;
    case 50:
      print("C+");
      break;
    case 40:
      print("C");
      break;
    case 30:
      print("F");
    default:
      print("Invalid grade");
  }


}